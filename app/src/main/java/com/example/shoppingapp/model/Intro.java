package com.example.shoppingapp.model;


public class Intro {

    private String id , link_lottie , description;


    public Intro() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getLink_lottie() {
        return link_lottie;
    }

    public void setLink_lottie(String link_lottie) {
        this.link_lottie = link_lottie;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
}
